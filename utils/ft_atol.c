long	ft_atol(char *str)
{
	int		neg;
	long	ret;

	neg = 0;
	ret = 0;
	while (*str <= ' ')
		++str;
	if (*str == '-' && str++)
		neg = 1;
	while (*str >= '0' && *str <= '9')
	{
		ret *= 10;
		ret += (*str - '0');
		++str;
	}
	if (neg)
		ret *= -1;
	return (ret);
}
